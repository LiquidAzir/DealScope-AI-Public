export function exportMemoAsPDF(memo, companyName) {
  if (!memo) return

  const html = marked.parse(memo)
  const date = new Date().toLocaleDateString('en-US', {
    year: 'numeric', month: 'long', day: 'numeric',
  })

  const page = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <title>${companyName} â€” Investment Memo</title>
  <style>${PRINT_STYLES}</style>
</head>
<body>
  <div class="doc-header">
    <div class="doc-logo">DealScope<span> AI</span></div>
    <div class="doc-meta">
      <strong>${companyName}</strong>
      Investment Memo &middot; ${date}
    </div>
  </div>
  ${html}
  <div class="doc-footer">
    Generated by DealScope AI &middot; ${date} &middot; Confidential &mdash; Not for Distribution
  </div>
</body>
</html>`

  const win = window.open('', '_blank', 'width=920,height=760,scrollbars=yes')
  if (!win) {
    alert('Please allow popups for this site to download the PDF.')
    return
  }
  win.document.write(page)
  win.document.close()
  win.focus()
  // Small delay so styles render before the print dialog opens
  setTimeout(() => win.print(), 450)
}
